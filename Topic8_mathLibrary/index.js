// Math = built-in object that provides a 
//        collection of properties and methods

let x = 3;
let y = 2;
let z = 1;

// ROUND: z = Math.round(x);
// FLOOR: z = Math.floor(x);
// CEIL: z = Math.ceil(x);
// TRUNC: z = Math.trunc(x);
// POW: z = Math.pow(x, y);
// SQRT: z = Math.sqrt(x);
// LOG: z = Math.log(x);
// SINE: z = Math.sin(x);
// COSINE: z = Math.cos(x);
// TAN: z = Math.tan(x);
// ABSOLUTE VALUE: z = Math.abs(x);
// SIGN: z = Math.sign(x);

// MAX: 
let max = Math.max(x, y, z);
// MIN: 
let min = Math.min(x, y, z);

console.log(max);
console.log(min);